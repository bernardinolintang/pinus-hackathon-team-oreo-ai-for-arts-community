export function getArtworkById(artworkId: string): ArtworkDetailItem | undefined {
  const num = parseInt(artworkId, 10);
  const idToUse =
    !isNaN(num) && numericIdToArtworkId[num]
      ? numericIdToArtworkId[num]
      : artworkId.trim();
  const slug = idToUse.toLowerCase().replace(/\s+/g, "-");
  return allArtworks.find(
    (art) =>
      art.artworkId === idToUse ||
      art.artworkId === slug ||
      art.artworkId.toLowerCase() === slug
  );
}
